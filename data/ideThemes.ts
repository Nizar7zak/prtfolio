export const DEFAULT_THEME_ID = "dark-plus";

export function getThemeById(id: string): IdeTheme {
  return IDE_THEMES.find((t) => t.id === id) ?? IDE_THEMES[0];
}

export function applyTheme(theme: IdeTheme) {
  const root = document.documentElement;
  const c = theme.colors;

  root.style.setProperty("--ide-bg", c.bg);
  root.style.setProperty("--ide-sidebar", c.sidebar);
  root.style.setProperty("--ide-topbar", c.topbar);
  root.style.setProperty("--ide-border", c.border);
  root.style.setProperty("--ide-line-num", c.lineNum);
  root.style.setProperty("--ide-comment", c.comment);
  root.style.setProperty("--ide-text", c.text);
  root.style.setProperty("--ide-accent", c.accent);
  root.style.setProperty("--ide-green", c.green);
  root.style.setProperty("--ide-tab-bg", c.tabBg);
  root.style.setProperty("--ide-tab-act", c.tabAct);
  root.style.setProperty("--ide-card", c.card);
  root.style.setProperty("--ide-card-hover", c.cardHover);
  root.style.setProperty("--ide-muted", c.muted);
  root.style.setProperty("--ide-scroll-thumb", c.scrollThumb);
  root.style.setProperty("--ide-scroll-thumb-hover", c.scrollThumbHover);
  root.style.setProperty("--ide-badge-bg", c.badgeBg);
  root.style.setProperty("--ide-button-bg", c.buttonBg);
  root.style.setProperty("--ide-button-text", c.buttonText);
  root.style.setProperty("--ide-keyword", c.keyword);
  root.style.setProperty("--ide-string", c.string);
  root.style.setProperty("--ide-function", c.function);
  root.style.setProperty("--ide-type", c.type);
  root.style.setProperty("--ide-number", c.number);
  root.style.setProperty("--ide-property", c.property);
  root.style.setProperty("--ide-syntax-comment", c.syntaxComment);
  root.style.setProperty("--ide-control-keyword", c.controlKeyword);

  root.setAttribute("data-theme", theme.id);
  root.style.colorScheme = theme.type;
}
